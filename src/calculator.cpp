// function to add two numbers
float calculate_add(float num1, float num2) {
   float result;
   result = num1 + num2; 
   return result;
}

// function to subtract two numbers
float calculate_substract(float num1, float num2) {
   float result;
   result = num1 - num2; 
   return result;
}

// function to divide two numbers
float calculate_divide(float num1, float num2) {
   float result;
   result = num1 / num2; 
   return result;
}

// function to multiply two numbers
float calculate_multiply(float num1, float num2) {
   float result;
   result = num1 * num2; 
   return result;
}

// function to reminder of a division of two values
float calculate_modulo(int num1, int num2) {
   int result;
   result = num1 % num2;
   return float(result);
}